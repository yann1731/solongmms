#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	cd;
	char	*ptr;

	cd = 0;
	ptr = (char *) s;
	while (cd < n)
	{
		*ptr = '\0';
		ptr++;
		cd++;
	}
}
